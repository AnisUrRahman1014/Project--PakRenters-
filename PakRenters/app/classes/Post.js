class Post {
  constructor(
    postID,
    vehicle,
    renter,
    description,
    views,
    comments,
    rating,
    status
  ) {
    this.postID = postID;
    this.vehicle = vehicle;
    this.renter = renter;
    this.description = description;
    this.views = views;
    this.comments = comments;
    this.rating = rating;
    this.status = status;
  }
}
